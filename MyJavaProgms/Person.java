public class Person {
	
	String name;
	String age;
	String contact;
	String address;
	String emp_Id;
	String salary;
	String height;
	String weight;
	
	public String toString() 
	{
		return name + " " + contact + " " + age + " " + address + " " + emp_Id + " " + salary + " " + height + " " + weight;
	}
	
}
